#include <bits/stdc++.h>
using namespace std;

int main(int argc, char const *argv[]) {
  unsigned short age = 70;  // variable age of size 16 bit and it's equal to 70
  int loan = 500; 
  unsigned long long money = 289328923;  // 0 -> 2^(32)
  long long balance = 3928392832923892;

  cout << age + 20 << endl;
  cout << loan << endl;
  cout << money << endl;
  cout << balance << endl;

  return 0;
}
